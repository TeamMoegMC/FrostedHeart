package com.teammoeg.frostedheart.util.client;

public class Point {
    protected final int x, y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    public static Point of(int x, int y) {
    	return new Point(x, y);
    }
}
